$(document).ready(function(){
    $("#green").click(function(){
// au clic change la couleur du texte en vert
        $("#texte").css("color","green");
    });
    $("#red").click(function(){
// au clic change la couleur du texte en rouge
        $("#texte").css("color","red");
    });
    $("#blue").click(function(){
// au clic change la couleur du texte en bleu
        $("#texte").css("color","blue");
    });
});
